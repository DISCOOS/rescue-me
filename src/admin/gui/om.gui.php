<h3>Om Røde Kors: savnet</h3>
<div class="lead">Savnet lokaliserer personer via deres mobiltelefon</div>
<p>Savnet sender en lenke til den savnede via SMS. Med brukerens samtykke (klikk på lenken) blir brukeren lokalisert enten via GPS-posisjonering eller GSM-posisjonering på telefonen. Alle posisjoner logges i systemet, også de med stor unøyaktighet.</p>
<p>Det sendes 2 SMS til brukeren; SMS 1 inneholder info om sporingslenke, SMS 2 inneholder info om GPS-innstillinger. Delingen sikrer at den savnede får den viktigste infoen først, og kan starte sporing fortest mulig. SMS 2 sendes kun hvis første posisjonering er unøyaktig, og kun én gang per savnet.</p>
<p>Posisjoneringssiden den savnede åpner er komprimert maksimalt for å kunne gi en raskest mulig innlasting, uansett internetthastighet. Selv med veldig dårlig internettilkobling skal siden lastes inn i løpet av sekunder.</p>

